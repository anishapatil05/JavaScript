// String Data Type    
var name = "Anisha";
console.log(name);
console.log(typeof name);

//  number Data Type
var id = 7;
console.log("id: " + id + " data type: " + typeof id);

// boolean Data type
var isPlaced = true;
console.log(isPlaced);

// undefined Data type
var name;
console.log(name);      // undefined;

// null Data Type
var name = null;
console.log(name)        // null;

var name = "Anu";
console.log(name);

